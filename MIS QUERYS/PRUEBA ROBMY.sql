SELECT VEN.SLPRSNID, VEN.SLPRSNFN, VEN.SPRSNSLN, CLI.CUSTNMBR, CLI.CUSTNAME, SUM(VH.DOCAMNT) AS [TOTAL CLIENTES]
FROM RM00301 VEN
INNER JOIN RM00101 CLI ON CLI.SLPRSNID=VEN.SLPRSNID 
INNER JOIN SOP30200 VH ON CLI.CUSTNMBR=VH.CUSTNMBR
WHERE VH.DOCDATE BETWEEN '2009.09.01' AND '2009.09.30'
AND VEN.SLPRSNID IN('1025','2591')
GROUP BY ---VEN.SLPRSNID, CLI.CUSTNMBR
VEN.SLPRSNID, VEN.SLPRSNFN, VEN.SPRSNSLN, CLI.CUSTNMBR, CLI.CUSTNAME
--* /
SELECT VEN.SLPRSNID, VEN.SLPRSNFN, VEN.SPRSNSLN, SUM(VH.DOCAMNT) AS [TOTAL VENDEDOR]
FROM RM00301 VEN
INNER JOIN SOP30200 VH ON VH.SLPRSNID=VEN.SLPRSNID
WHERE VH.DOCDATE BETWEEN '2009.09.01' AND '2009.09.30'
AND VEN.SLPRSNID IN('1025','2591')
GROUP BY ---VEN.SLPRSNID, CLI.CUSTNMBR
VEN.SLPRSNID, VEN.SLPRSNFN, VEN.SPRSNSLN


