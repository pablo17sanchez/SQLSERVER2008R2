SELECT	P.VENDORID Vendor_ID,
	V.VENDNAME Vendor_Nombre,
	P.VCHRNMBR Voucher,
	CASE P.DOCTYPE
	     WHEN 1 THEN 'Factura'
	     WHEN 2 THEN 'Cargo Financiero'
     	     WHEN 3 THEN 'Cargo Miscelanio'
     	     WHEN 4 THEN 'Devolucion'
     	     WHEN 5 THEN 'Nota Credito'
	     WHEN 6 THEN 'Pago'
	     END Document_Tipo,
	P.DOCDATE Document_Fecha,
	P.DOCNUMBR Document_Num,
	DEBITAMT Debito,
	CRDTAMNT Credito,
	P.TRXDSCRN Descrip_transac,
	'00-113020000-00'Cuenta,
	CASE P.VOIDED
	     WHEN 0 THEN 'No'
	     WHEN 1 THEN 'Yes'
	     END Anulado

FROM	(SELECT VENDORID, VCHRNMBR, DOCTYPE, DOCDATE, PSTGDATE,
	 DUEDATE, DOCNUMBR, DOCAMNT, CURTRXAM, TRXDSCRN, VOIDED, DINVPDOF
	 FROM PM20000
	UNION ALL
	 SELECT VENDORID, VCHRNMBR, DOCTYPE, DOCDATE, PSTGDATE,
	 DUEDATE, DOCNUMBR, DOCAMNT, CURTRXAM, TRXDSCRN, VOIDED, DINVPDOF
	 FROM PM30200) P

     INNER JOIN
	PM00200 V
	ON V.VENDORID = P.VENDORID
	INNER JOIN (SELECT VCHRNMBR,CRDTAMNT,DEBITAMT,DSTINDX,VENDORID,TRXSORCE,CNTRLTYP,PSTGDATE FROM PM30600
				WHERE DSTINDX=33
				UNION ALL 
				SELECT VCHRNMBR,CRDTAMNT,DEBITAMT,DSTINDX,VENDORID,TRXSORCE,CNTRLTYP,PSTGDATE FROM PM10100
				WHERE DSTINDX=33
)PINDX ON P.VCHRNMBR=PINDX.VCHRNMBR AND P.VENDORID=PINDX.VENDORID
WHERE YEAR(P.DOCDATE)=2015 




